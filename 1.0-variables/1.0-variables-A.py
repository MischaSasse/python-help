# Een variabel is een container in het geheugen waar een waarde aan vast kan komen.
myVar = 2.42
print(myVar, type(myVar))

myVar = "Hello World!"
print(myVar, type(myVar))

myVar = 2
print(myVar, type(myVar))

myVar = True
print(myVar, type(myVar))

myVar = []
print(myVar, type(myVar))

myVar = {}
print(myVar, type(myVar))

# Ter uitleg van wat hierboven staat: print() print iets naar je console. 
# type() geeft het type van een meegegeven waarde weer